func encode(image []byte) []byte {
	b := image[0 : 224*224]
	g := image[224*224 : 2*224*224]
	r := image[2*224*224 : 3*224*224]
	encoded := make([]byte, 4*3*224*224)
	var i = 0
	for x := 0; x < 16; x++ {
		for y := 0; y < 16; y++ {
			i = encodeTile(b, i, y, x, encoded)
			i = encodeTile(g, i, y, x, encoded)
			i = encodeTile(r, i, y, x, encoded)
		}
	}
	return encoded
}

func encodeTile(plane []byte, i int, y int, x int, encoded []byte) int {
	for xx := 0; xx < 14; xx++ {
		for yy := 0; yy < 14; yy++ {
			//encoded[i] = plane[(y*14+yy)*224+(x*14+xx)]
			encoded[i] = plane[(y*14+yy)+(x*14+xx)*224]
			i++
			encoded[i] = 0x03
			i++
			encoded[i] = 0x00
			i++
			encoded[i] = 0x00
			i++
		}
	}
	return i
}
